import UIKit

extension UIColor {
    static func blueColor() -> UIColor { return UIColor(named: "BlueColor")! }
    static func whiteColor() -> UIColor { return UIColor(named: "WhiteColor")! }
    static func blackTextColor() -> UIColor { return UIColor(named: "BlackTextColor")! }
}
